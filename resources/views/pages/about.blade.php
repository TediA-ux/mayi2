                        <p>By August, 2013, the total membership had reached 81 members with 18 men and 62 women. Currently we have over 300 members and more 
                            The organization has executed several activities/projects in the field of climate change adaptation and mitigation among others projects under INBAR , UWA ,CDD and NAADS.
                            Its co-partner organization which is an arm implementing  the climate  resilience projects Aw Bamboo Enterprises Ltd was registered on 2012 so as to help in direct running of the social arm of business as it supports community while ensuring Business sustainability through value addition to different products ranging from Bamboo to coffee other products too.</p>
                        <h2>The Value Proposition</h2>
                        <p>Mayi and AW Bamboo Enterprises have  come in to bridge this big gap and ease the above tensions as we  meet the two key SDGs of Environmental conservation and Poverty eradication in the Mt. Elgon sub region. We have embarked on a big drive to grow highland  Bamboo  on the Mt Elgon slopes in partnership with UWA given that Bamboo is a  great  natural carbon sink to ensure sustainable  environmental conservation and growth of Bamboo in the region .</p>
                        <p>Therefore the project will promote different species but mainly Bamboo  tree planting as solution reversing deforestation trends while beekeeping will not only contribute to household income but also biodiversity conservation through pollination functions.</p>
